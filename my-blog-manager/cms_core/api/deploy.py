@router.post("/init")
async def init_deploy_env(request: Request):
    """初始化双轨部署环境"""
    try:
        payload = await request.json()
        blog_path = payload.get("blogPath", "").strip()
        static_repo = payload.get("staticRepoUrl", "").strip()

        if not blog_path or not os.path.exists(blog_path):
            return {"success": False, "message": "目标博客路径不存在！"}

        # 1. 基础 Git 初始化
        subprocess.run(["git", "init"], cwd=blog_path, check=True)

        # 为了兼容老版的静态部署插件，我们把 origin 绑定给 A线 (静态仓库)
        if static_repo:
            subprocess.run(["git", "remote", "remove", "origin"], cwd=blog_path, stderr=subprocess.DEVNULL)
            subprocess.run(["git", "remote", "add", "origin", static_repo], cwd=blog_path, check=True)

        # 2. 修改 package.json 写入 scripts (专供 A 线静态发布)
        pkg_path = os.path.join(blog_path, "package.json")
        if os.path.exists(pkg_path):
            with open(pkg_path, "r", encoding="utf-8") as f:
                pkg_data = json.load(f)
            if "scripts" not in pkg_data:
                pkg_data["scripts"] = {}
            pkg_data["scripts"]["deploy"] = "next build && gh-pages -d out"
            with open(pkg_path, "w", encoding="utf-8") as f:
                json.dump(pkg_data, f, indent=2, ensure_ascii=False)

        # 3. 安装 gh-pages 插件
        process = subprocess.Popen(
            ["npm", "install", "gh-pages", "--save-dev"],
            cwd=blog_path,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            shell=True,
            text=True,
            encoding='utf-8'
        )
        stdout, stderr = process.communicate()

        return {"success": True, "message": "✨ 太棒了！双轨部署底层依赖与配置初始化完成！"}
    except Exception as e:
        return {"success": False, "message": f"初始化发生异常: {str(e)}"}


# 🔄 一键部署：构建 → 复制 → 推送 gh-pages
@router.post("/trigger-workflow")
async def trigger_workflow():
    """完整部署流程: npm build → 复制out/到XHBlogs → git push gh-pages"""
    import shutil
    try:
        # 读取配置
        if not os.path.exists(CONFIG_FILE):
            return {"success": False, "message": "未找到 deploy_config.json"}
        with open(CONFIG_FILE, "r", encoding="utf-8") as f:
            config = json.load(f)
        blog_path = config.get("blogPath", "").strip()
        if not blog_path or not os.path.exists(blog_path):
            return {"success": False, "message": f"blogPath 不存在或为空: {blog_path}"}

        # Step 1: 临时移除 app/api（静态导出不支持动态 API 路由）
        api_dir = os.path.join(PROJECT_ROOT, "app", "api")
        api_backup = os.path.join(PROJECT_ROOT, "app", "_api_backup")
        api_moved = False
        if os.path.exists(api_dir):
            os.rename(api_dir, api_backup)
            api_moved = True
            print(f"[Deploy] Step1a: moved app/api -> app/_api_backup")

        try:
            # Step 2: npm run build
            print(f"[Deploy] Step1b: building Next.js at {PROJECT_ROOT}...")
            build_result = subprocess.run(
                ["npm", "run", "build"],
                cwd=PROJECT_ROOT,
                capture_output=True,
                text=True,
                encoding="utf-8",
                timeout=300,
                shell=True
            )
            if build_result.returncode != 0:
                return {"success": False, "message": f"构建失败:\n{build_result.stderr[-2000:]}"}
            print(f"[Deploy] Step1b: build OK")
        finally:
            # 恢复 app/api
            if api_moved:
                os.rename(api_backup, api_dir)
                print(f"[Deploy] Step1c: restored app/api")

        # Step 2: 清空 blog_path (保留 .git) 并复制 out/*
        out_dir = os.path.join(blog_path, "dist")
        if not os.path.exists(out_dir):
            return {"success": False, "message": f"out/ 目录不存在: {out_dir}"}
        print(f"[Deploy] Step2: copying {out_dir} -> {blog_path}...")
        for item in os.listdir(blog_path):
            item_path = os.path.join(blog_path, item)
            if item == ".git": continue
            if os.path.isdir(item_path):
                shutil.rmtree(item_path)
            else:
                os.remove(item_path)
        for item in os.listdir(out_dir):
            src = os.path.join(out_dir, item)
            dst = os.path.join(blog_path, item)
            if os.path.isdir(src):
                shutil.copytree(src, dst)
            else:
                shutil.copy2(src, dst)
        print(f"[Deploy] Step2: copy OK")

        # Step 3: git checkout gh-pages → add → commit → push → checkout main
        print(f"[Deploy] Step3: git push to gh-pages...")
        subprocess.run(["git", "checkout", "gh-pages"], cwd=blog_path, check=True, capture_output=True, shell=True)
        subprocess.run(["git", "add", "."], cwd=blog_path, check=True, capture_output=True, shell=True)
        commit_result = subprocess.run(
            ["git", "commit", "-m", "auto-deploy from CMS"],
            cwd=blog_path, capture_output=True, text=True, shell=True
        )
        push_result = subprocess.run(
            ["git", "push", "origin", "gh-pages"],
            cwd=blog_path, capture_output=True, text=True, timeout=120, shell=True
        )
        subprocess.run(["git", "checkout", "main"], cwd=blog_path, capture_output=True, shell=True)

        if push_result.returncode != 0 and "Everything up-to-date" not in push_result.stderr:
            return {"success": False, "message": f"git push 失败:\n{push_result.stderr}"}

        return {"success": True, "message": "🎉 部署成功！构建→复制→推送全部完成，GitHub Pages 将自动更新。"}
    except subprocess.TimeoutExpired:
        return {"success": False, "message": "操作超时（可能是构建或push太慢）"}
    except Exception as e:
        return {"success": False, "message": f"部署异常: {str(e)}"}


