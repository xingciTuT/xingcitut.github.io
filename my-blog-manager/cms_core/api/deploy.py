# ☁️ B 线接口：同步源代码到 GitHub → Vercel 自动构建
@router.post("/source")
async def sync_source_to_vercel(request: Request):
    try:
        payload = await request.json()
        blog_path = payload.get("blogPath", "").strip()

        # 读取配置
        if not os.path.exists(CONFIG_FILE):
            return {"success": False, "message": "未找到配置文件，请先点击保存！"}

        with open(CONFIG_FILE, "r", encoding="utf-8") as f:
            config = json.load(f)

        source_repo = config.get("sourceRepoUrl", "").strip()
        source_branch = config.get("sourceBranch", "main").strip()

        if not source_repo:
            return {"success": False, "message": "B 线源码仓库地址为空，无法同步！"}

        # 确保 repo URL 是 HTTPS 格式
        if source_repo.startswith("git@"):
            # git@github.com:user/repo.git → https://github.com/user/repo.git
            source_repo = source_repo.replace("git@github.com:", "https://github.com/").replace(".git", ".git")

        # 1. 确保在 my-blog-manager 目录下操作
        work_dir = PROJECT_ROOT

        # 2. git add
        subprocess.run(["git", "add", "."], cwd=work_dir, capture_output=True, shell=True)

        # 3. git commit（无改动时跳过）
        subprocess.run(
            ["git", "commit", "-m", "Auto sync source code for Vercel"],
            cwd=work_dir, capture_output=True, shell=True
        )

        # 4. 确保 remote origin 指向正确的 HTTPS 仓库
        subprocess.run(["git", "remote", "remove", "origin"], cwd=work_dir, capture_output=True, shell=True)
        subprocess.run(
            ["git", "remote", "add", "origin", source_repo],
            cwd=work_dir, check=True, capture_output=True, shell=True
        )

        # 5. git push（HTTPS + PAT，凭证由 credential.helper manager 提供）
        push_result = subprocess.run(
            ["git", "push", "origin", f"HEAD:{source_branch}", "--force"],
            cwd=work_dir, capture_output=True, text=True, encoding="utf-8", timeout=120, shell=True
        )

        if push_result.returncode != 0 and "Everything up-to-date" not in push_result.stdout and "Everything up-to-date" not in push_result.stderr:
            return {"success": False, "message": f"源码同步失败:\n{push_result.stdout[-1500:]}\n{push_result.stderr[-1500:]}"}

        return {"success": True, "message": "☁️ B线: 源码已成功推送到 GitHub，Vercel 将自动构建部署！"}

    except subprocess.TimeoutExpired:
        return {"success": False, "message": "推送超时，请检查网络连接"}
    except Exception as e:
        return {"success": False, "message": f"源码同步引擎异常: {str(e)}"}